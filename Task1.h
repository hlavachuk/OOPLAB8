#define TASK1_H               // якщо ні — "помічаю", що тепер підключений

#include <array>              // підключаю масиви типу array
#include <iterator>           // підключаю штуки для роботи з ітераторами (типу next)

using namespace std;          // щоб не писати постійно std::

template <typename T, size_t N>      // роблю шаблон, щоб працювало з будь-яким типом і розміром
void customsort(array<T, N>& arr) {  // функція сортування масиву (передаю його напряму)

    if (N < 2) return;      // якщо там 0 або 1 елемент — сортувати нема що

    for (auto i = arr.begin(); i != arr.end(); i++) {   // просто роблю кілька проходів по масиву
        for (auto j = arr.begin(); next(j) != arr.end(); j++) {  // проходжусь по елементах

            if (*next(j) < *j) {   // якщо наступний елемент менший ніж поточний
                T temp = *j;      // зберігаю поточний
                *j = *next(j);    // ставлю на його місце наступний
                *next(j) = temp;  // а на місце наступного — старий
            }
        }
    }
}

template <typename T, size_t N, size_t M>   // ще один шаблон для двох масивів
array <T, N + M> mergeArrays(array<T, N> a, array<T, M> b) {  // функція, що зливає два масиви

    customsort(a);   // спочатку сортую перший масив
    customsort(b);   // потім другий

    array <T, N + M> result;  // створюю новий масив, куди все складатиму

    auto A = a.begin();  // ставлю "вказівник" на початок першого масиву
    auto B = b.begin();  // на початок другого
    auto R = result.begin();  // і на початок результату

    while (A != a.end() && B != b.end()) {  // поки ще є елементи в обох масивах

        if (*A < *B) {   // якщо елемент з першого менший
            *R = *A;     // записую його в результат
            A++;         // переходжу до наступного в першому
        }
        else {           // інакше беру з другого
            *R = *B;
            B++;
        }
        R++;             // рухаюсь далі в результаті
    }

    while (A != a.end()) {   // якщо в першому ще щось залишилось
        *R = *A;             // просто копіюю в результат
        A++;
        R++;
    }

    while (B != b.end()) {   // якщо в другому ще щось залишилось
        *R = *B;             // теж копіюю
        B++;
        R++;
    }

    return result;   // повертаю готовий масив
}

